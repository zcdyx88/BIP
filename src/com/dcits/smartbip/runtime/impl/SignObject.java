package com.dcits.smartbip.runtime.impl;

public class SignObject {
	private String productId;	
	private String productName;
	private String serviceId;
	//已签约返回标识
	private String signingRetCode;
	
	public SignObject(String productId, String serviceId, String productName) {
		super();
		this.productId = productId;
		this.productName = productName;
		this.serviceId = serviceId;
	}
	public SignObject(String productId, String serviceId, String productName ,String signingRetCode) {
		super();
		this.productId = productId;
		this.productName = productName;
		this.serviceId = serviceId;
		this.signingRetCode = signingRetCode; 
	}
	public String getSigningRetCode() {
		return signingRetCode;
	}
	public void setsigningRetCode(String signingRetCode) {
		this.signingRetCode = signingRetCode;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getServiceId() {
		return serviceId;
	}
	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}
}

