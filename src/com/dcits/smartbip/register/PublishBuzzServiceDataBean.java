package com.dcits.smartbip.register;

import java.io.Serializable;
import java.util.List;

public class PublishBuzzServiceDataBean implements Serializable {

    private static final long serialVersionUID = 1L;

    String id;
    String protocolType;
    String retCode;
    String namespace;
    String reversalServiceId;
    String wsdl;
    String serviceSchema;
    List<String> metadataSchema;

    public PublishBuzzServiceDataBean(String id, String protocolType, String retCode, String namespace, String reversalServiceId, String wsdl, String serviceSchema, List<String> metadataSchema) {
        super();
        this.id = id;
        this.protocolType = protocolType;
        this.retCode = retCode;
        this.namespace = namespace;
        this.reversalServiceId = reversalServiceId;
        this.wsdl = wsdl;
        this.serviceSchema = serviceSchema;
        this.metadataSchema = metadataSchema;
    }

}
